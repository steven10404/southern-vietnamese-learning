function mergeByKey(existing, additions, keyFn) {
  const seen = new Set(existing.map(keyFn));
  for (const item of additions) {
    const key = keyFn(item);
    if (!seen.has(key)) {
      existing.push(item);
      seen.add(key);
    }
  }
}

